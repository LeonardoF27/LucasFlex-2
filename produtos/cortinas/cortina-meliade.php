<h1>Cortina Melíade</h1>

<p>A cortina Melíade proporciona leveza e sofisticação aos ambientes. Possui delicadas lâminas de tecido que refletem a luz ultravioleta, impedindo que a claridade entre no ambiente, proporcionando privacidade.  
As lâminas são confeccionadas com dois tipos de tecidos,  opaco e translúcidos que com a movimentação é possível controlar a luminosidade. 
</p>

<p><b>Indicado para ambientes:</b> Residenciais e comerciais.</p>

<p><b>Acionamento:</b> Manual e motorizada. Com opção de motor a bateria.</p>

