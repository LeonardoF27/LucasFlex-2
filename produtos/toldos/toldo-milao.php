<h1>Toldo Milão</h1>

<p>O Toldo Milão é o tradicional toldo de braços articulado, este modelo foi desenvolvido para criar e proteger ambientes externos do sol. Protege também de chuvas moderadas, sereno e ventos fracos de até 38 km/h. </p>

<p><b>Indicados para ambientes:</b> Residenciais e comerciais.</p>

<p><b>Acionamentos:</b> Manual e Motorizado.</p>

